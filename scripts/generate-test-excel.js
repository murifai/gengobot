// Function to create workbook with formatting
function createWorkbook(data, filename) {
  const wb = XLSX.utils.book_new();

  Object.keys(data).forEach(sheetName => {
    const ws = XLSX.utils.json_to_sheet(data[sheetName]);

    // Set column widths
    const colWidths = [
      { wch: 15 }, // ID
      { wch: 45 }, // Task
      { wch: 15 }, // Module
      { wch: 10 }, // Priority
      { wch: 30 }, // Precondition
      { wch: 40 }, // Expected Result
      { wch: 30 }, // Actual Result
      { wch: 12 }, // Status
      { wch: 15 }, // Tester
      { wch: 12 }, // Date
      { wch: 30 }, // Notes
    ];
    ws['!cols'] = colWidths;

    XLSX.utils.book_append_sheet(wb, ws, sheetName.substring(0, 31)); // Excel sheet name limit
  });

  // Write file
  const outputPath = path.join(__dirname, '..', 'docs', 'testing', filename);
  XLSX.writeFile(wb, outputPath);
  console.log(`Created: ${outputPath}`);
}

